package model;

/**
 *
 * @author ADMIN
 */
public class Bill {

    private int tableId;
    private int dishId;
    private int quantity;
    private int price;

    public Bill() {
    }

    public Bill(int tableId, int dishId, int quantity, int price) {
        this.tableId = tableId;
        this.dishId = dishId;
        this.quantity = quantity;
        this.price = price;
    }

    public int getTableId() {
        return tableId;
    }

    public void setTableId(int tableId) {
        this.tableId = tableId;
    }

    public int getDishId() {
        return dishId;
    }

    public void setDishId(int dishId) {
        this.dishId = dishId;
    }

    public int getQuantity() {
        return quantity;
    }

    public void setQuantity(int quantity) {
        this.quantity = quantity;
    }

    public int getPrice() {
        return price;
    }

    public void setPrice(int price) {
        this.price = price;
    }

    
}
